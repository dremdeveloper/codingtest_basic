import java.util.*;

class GN01VariableType {
    public static void main(String[] args) {
        int number = 10;
        double pi = 3.14;
        String text = "python";
        boolean flag = true;

        System.out.println("number 타입: int 값: " + number);
        System.out.println("pi 타입: double 값: " + pi);
        System.out.println("text 타입: String 길이: " + text.length());
        System.out.println("flag 타입: boolean 값: " + flag);
    }
}
