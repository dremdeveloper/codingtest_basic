"""1) 변수/자료형: 코딩 테스트에서 자주 쓰는 기본 타입 예시."""

# 파이썬은 변수 선언 시 타입을 명시하지 않아도 됩니다.
number = 10          # int
pi = 3.14            # float
text = "python"      # str
flag = True          # bool

print("number 타입:", type(number), "값:", number)
print("pi 타입:", type(pi), "값:", pi)
print("text 타입:", type(text), "길이:", len(text))
print("flag 타입:", type(flag), "값:", flag)
